# Ler os CSV para DataFrames
df_tempos = pd.read_csv(StringIO(csv_tempos))
df_rotacoes = pd.read_csv(StringIO(csv_rotacoes))

# Vamos criar dicionários para mapear os arrays:
# Para tempos, note que a coluna "Array" pode ter valores "Keys_A" (para binary tree) e "Array_A" (para as demais).
# Vamos incluir ambos nos mesmos gráficos, renomeando "Keys_A" para "Array_A" para uniformizar.
def normalize_array(row):
    if row["Tipo de Árvore"] == "BINARY TREE TRANSVERSAL":
        # Trocar "Keys_X" por "Array_X": basta substituir "Keys" por "Array"
        return row["Array"].replace("Keys", "Array")
    else:
        return row["Array"]

df_tempos["Array"] = df_tempos.apply(normalize_array, axis=1)

# Listas dos arrays que vamos processar:
arrays = ["Array_A", "Array_B", "Array_C", "Array_D"]

# Criar um Excel com 4 folhas para tempos
with pd.ExcelWriter("Tempos.xlsx") as writer:
    for arr in arrays:
        # Filtrar linhas para o array arr
        df_arr = df_tempos[df_tempos["Array"] == arr].copy()
        # Pivotar os dados para ter colunas: Número de Keys e cada Tipo de Árvore como coluna
        pivot = df_arr.pivot(index="Número de Keys", columns="Tipo de Árvore", values="Tempo médio (s)")
        pivot = pivot.sort_index()
        pivot.to_excel(writer, sheet_name=arr)

# Criar um Excel com 4 folhas para rotações
with pd.ExcelWriter("Rotacoes.xlsx") as writer:
    for arr in arrays:
        df_arr = df_rotacoes[df_rotacoes["Array"] == arr].copy()
        pivot = df_arr.pivot(index="Número de Keys", columns="Tipo de Árvore", values="Rotações médias")
        pivot = pivot.sort_index()
        pivot.to_excel(writer, sheet_name=arr)

print("Ficheiros 'Tempos.xlsx' e 'Rotacoes.xlsx' criados com 4 folhas cada (Array_A, Array_B, Array_C, Array_D).")
